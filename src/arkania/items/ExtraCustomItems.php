declare(strict_types=1);

namespace arkania\items;

use arkania\items\pvp\Soupe;
use arkania\items\pvp\SwitchStick;
use pocketmine\item\Item;
use pocketmine\item\ItemIdentifier;
use pocketmine\item\ItemTypeIds;
use pocketmine\utils\CloningRegistryTrait;

/**
 * This doc-block is generated automatically, do not modify it manually.
 * This must be regenerated whenever registry members are added, removed or changed.
 * @see build/generate-registry-anotation.php
 * @generate-registry-docblock
 *
 * @method static ItemTest ITEM_TEST()
 * @method static Soupe SOUPE()
 * @method static SwitchStick SWITCH_STICK()
 */
class ExtraCustomItems {
	use CloningRegistryTrait;

	protected static function register(string $name, Item $item) : void {
		self::_registryRegister($name, $item);
	}

	/**
	 * @return Item[]
	 * @phpstan-return array<string, Item>
	 */
	public static function getAll() : array {
		//phpstan doesn't support generic traits yet :(
		/** @var Item[] $result */
		$result = self::_registryGetAll();
		return $result;
	}

	protected static function setup() : void {
		self::register("item_test", new ItemTest(new ItemIdentifier(ItemTypeIds::newId()), "Item Test"));
        self::register("soupe", new Soupe(new ItemIdentifier(ItemTypeIds::newId()), "Soupe"));
        self::register("switch_stick", new SwitchStick(new ItemIdentifier(ItemTypeIds::newId()), "Switch Stick"));
	}

}
